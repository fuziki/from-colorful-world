import SwiftUI

extension Color {
    static var systemGroupedBackground: Color {
        return Color(UIColor.systemGroupedBackground)
    }
    
    static var secondarySystemGroupedBackground: Color {
        return Color(UIColor.secondarySystemGroupedBackground)
    }
}
